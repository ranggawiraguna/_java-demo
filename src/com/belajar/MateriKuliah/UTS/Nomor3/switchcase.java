public class switchcase {
    public static void main(String[] args) {
        int pilih = 3;
        switch(pilih){
            case 1 :
                System.out.println("Hobi Saya Membaca");
                break;
            case 2 :
                System.out.println("Hobi Saya Melukis");
                break;
            case 3 :
                System.out.println("Hobi Saya Travelling");
                break;
        }        
    }
}